function initLangue() {
    var languageNav =
        window.navigator.userLanguage || window.navigator.language;
    if (
        languageNav.substr(0, 2) == "pt" ||
        languageNav.substr(0, 2) == "fr" ||
        languageNav.substr(0, 2) == "es" ||
        languageNav.substr(0, 2) == "it" ||
        languageNav.substr(0, 2) == "se" ||
        languageNav.substr(0, 2) == "jp" ||
        languageNav.substr(0, 2) == "no" ||
        languageNav.substr(0, 2) == "fi" ||
        languageNav.substr(0, 2) == "da" ||
        languageNav.substr(0, 2) == "jp" ||
        languageNav.substr(0, 2) == "de" ||
        languageNav.substr(0, 2) == "pl" ||
        languageNav.substr(0, 2) == "hu" ||
        languageNav.substr(0, 2) == "ru"
    ) {
        setLangue(languageNav.substr(0, 2));
    } else {
        setLangue("gb");
    }
}
